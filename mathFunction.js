// Absolute value
const absoluteValue = Math.abs(-5); // Returns 5

// Ceiling (round up)
const roundedUp = Math.ceil(4.2); // Returns 5

// Floor (round down)
const roundedDown = Math.floor(4.9); // Returns 4

// Round to the nearest integer
const roundedNumber = Math.round(4.5); // Returns 5

// Maximum value
const maxValue = Math.max(3, 7, 1, 9); // Returns 9

// Minimum value
const minValue = Math.min(3, 7, 1, 9); // Returns 1

// Power
const powerResult = Math.pow(2, 3); // Returns 8

// Square root
const squareRoot = Math.sqrt(9); // Returns 3

// Random number between 0 (inclusive) and 1 (exclusive)
const randomNumber = Math.random(); // Returns a random number between 0 and 1

// Mathematical constant Pi (Ï€)
const piValue = Math.PI; // Returns 3.141592653589793

// Mathematical constant Euler's number (e)
const eValue = Math.E; // Returns 2.718281828459045

// Trigonometric functions
const sineValue = Math.sin(Math.PI / 2); // Returns 1 (sine of 90 degrees)
const cosineValue = Math.cos(0); // Returns 1 (cosine of 0 degrees)
const tangentValue = Math.tan(Math.PI / 4); // Returns 1 (tangent of 45 degrees)

